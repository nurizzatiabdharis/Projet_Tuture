			<h2 id='intro'>Un pays multi-culturel</h2>
			<p>Le pays est multi-ethnique et multi-culturel, ce qui joue un grand rôle dans la politique. 
			Environ la moitié de la population sont les malais, avec de grandes minorités de chinois malais, des Indiens malais et des peuples indigènes.
			La constitution déclare l'Islam comme religion d'état et permet la liberté de religion pour les non-musulmans. 
			Le système gouvernemental est étroitement modelé sur Westminster, le système parlementaire et le système légal sont eux basés sur le droit coutumier. 
			</p>
