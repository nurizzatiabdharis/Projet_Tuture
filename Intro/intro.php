			<h2 id='intro'>Géographie</h2>
			</p>La Malaisie est une fédération composée de 13 États et trois districts fédéraux, divisée en deux régions distinctes, à savoir la Malaisie occidentale (ou péninsulaire) au sud de la Thaïlande et la Malaisie orientale située au nord de l’Indonésie.
				Le pays est relié à Singapour par deux ponts traversant le détroit de Johor. La Malaisie péninsulaire partage le même terrain et la même frontière maritime avec la Thaïlande et aussi des frontières maritimes avec Singapour, le Viêt-Nam et l'Indonésie. 
				De plus, elle partage également le terrain (la terre) et des frontières maritimes avec Brunei, l'Indonésie et une frontière maritime avec les Philippines et le Viêt-Nam .
