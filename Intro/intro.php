			<h2 id='intro'>Langues parlées en Malaisie</h2>
			<p>Les différentes ethnies présentes en Malaisie ont chacune une langue particulière. 
			Si le malais est la langue officielle, l’anglais arrive en seconde position, maîtrisé par la majeure partie de la population et notamment par les personnes impliquées dans le monde des affaires.
			Certains uilisent également l'anglais comme langue maternelle.
			Au total, plus de 130 langues sont parlées en Malaisie et 4 sont plus courrantes :  l'anglais, le malais, le chinois et le tamoul.
			</p>

			<h2 id='intro'>Le roi et le gouvernement</h2>
			<p>La Malaisie est une monarchie fédérale parlementaire multipartite, où le Premier ministre est le chef du gouvernement. 
			Le pouvoir exécutif est détenu par le gouvernement tandis que le pouvoir législatif est partagé entre le gouvernement et le parlement.
			</p>
