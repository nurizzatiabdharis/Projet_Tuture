
				<p><h1> Kata Bantu <i>(Les temps)</i> </h1></p>
				<p>En français, on exprime les temps dans une phrase en conjugant le verbe principal par rapport aux temps.</p>
        <p>Chaque temps a son propre regle de conjugaison. En malais, il n'y a pas de conjugaison. On utilise un mot pour exprimer un temps.</p>

