<h3>Négation - Kata Nafi</h3>
	<p>On va apprendre comment on dit les phrases negation en malais. </p>
	<p>Il existe plusieurs possibilités en malais<br>
	<h4>1) tidak </h4>
	<p>On utilise "tidak" avec cette règle: ***sujet + <strong>tidak + verbe </strong></p>
	<h4>2) bukan </h4>
	<p>On utilise "bukan" avec cette règle: ***sujet + <strong>bukan + nom </strong></p>
	<h4>3) Jangan</h4>
	<p>En general on utlise "jangan" pour exprimer un conseil ou une instruction </p>
	<p>Attention! On le met toujours au début de la phrase, suivi toujours d'un verbe.
	<h4>4) tiada</h4>
	<p>On utilise "tiada" avec cette règle: ***sujet + <strong>tiada + nom/object/compléments </strong></p>
	<p>Attention! On n'utilise jamais "tiada" suivi d'un verbe</p>
	<p>Exemple ne pas commettre: saya <i>tiada</i> tidur semalam </p>	
	<p>correction: saya <i>tidak</i> tidur semalam.</p>
	<h4>5) tak</h4>
	<p>En fait, "tak" peut être utilisé comme "tidak" et aussi "tiada" mais seulement dans l'oral, pas dans l'écriture.</p> 
	<p>Attention!! pour exprimer "tiada" on utilise avec cette règle: ***sujet + <strong>tak + ada + nom/object/compléments</strong>
	<p>Exemple: Saya <i>tiada</i> masa</p>
	<p>Exemple ne pas commettre: saya <i>tak</i> masa</p>	
	<p>correction: saya <i>tak ada</i> masa</p>
